#!/usr/local/bin/node


const {
    promises: fs,
} = require('fs');

const path = require('path');



/**
 * Characters that must never appear in a value that is interpolated raw into a
 * `Dockerfile` instruction line (`FROM`, `ARG`, `ENV`, `WORKDIR`, ...).
 *
 * A carriage return or newline would terminate the current instruction and let
 * the remainder of the value inject arbitrary extra `Dockerfile` directives.
 * Quotes are rejected as well, since a quote in these positions is never
 * legitimate and only serves to break out of the intended token.
 */
const INJECTION_PATTERN = /[\r\n"']/;


const assertNoInjection = (
    value,
    field,
    index,
) => {
    const text = String(value);

    if (INJECTION_PATTERN.test(text)) {
        throw new Error(
            `Stage ${index}: illegal character in '${field}'. `
            + `Carriage returns, newlines, and quotes are not allowed `
            + `(possible Dockerfile injection): ${JSON.stringify(text)}`,
        );
    }

    return text;
}


const validateParsedData = (
    data,
) => {
    if (!Array.isArray(data)) {
        return false;
    }

    for (const item of data) {
        if (
            !Array.isArray(item)
            && typeof item !== 'object'
        ) {
            return false;
        }
    }

    return true;
}


const composeStage = (
    index,
    stage,
) => {
    if (!stage.imagene) {
        // A stage without a base image cannot produce a valid `FROM`
        // instruction. Previously this branch did a bare `return;`, handing
        // back `undefined`, which the caller concatenated as the literal
        // string "undefined". Fail loudly instead so the error propagates.
        throw new Error(`Stage ${index} has no imagene (base image).`);
    }

    let stageString = `# Stage ${index}\n`;

    stageString += 'FROM ' + assertNoInjection(stage.imagene, 'imagene', index) + '\n';

    if (stage.arguments) {
        stage.arguments.forEach(value => {
            stageString += 'ARG ' + assertNoInjection(value, 'arguments', index) + '\n';
        });
    }

    if (stage.environment) {
        for (const [key, value] of Object.entries(stage.environment)) {
            const cleanKey = assertNoInjection(key, 'environment key', index);
            const cleanValue = assertNoInjection(value, 'environment value', index);
            stageString += 'ENV ' + cleanKey + ' ' + cleanValue + '\n';
        }
    }

    if (stage.directory) {
        stageString += 'WORKDIR ' + assertNoInjection(stage.directory, 'directory', index) + '\n';
    }

    if (stage.actions) {
        stageString += stage.actions.join('\n');
        stageString += '\n';
    }

    if (stage.literals) {
        stageString += stage.literals.join('\n');
        stageString += '\n';
    }

    if (stage.command) {
        // Emit `CMD` in JSON exec-array form. `JSON.stringify` safely escapes
        // quotes, spaces, and newlines inside each argument, so a command such
        // as `echo "hi there"` cannot break out of the array or inject.
        const command = Array.isArray(stage.command)
            ? stage.command
            : [stage.command];

        stageString += 'CMD ' + JSON.stringify(command) + '\n';
    }

    return stageString;
}


const generateDockerText = (
    data,
) => {
    if (!validateParsedData(data)) {
        throw new Error(
            `Invalid data: expected a list of stages (arrays or objects).`,
        );
    }

    let dockerText = `# Dockerfile generated by 'docker-deon'.\n\n`;

    for (const [index, stage] of data.entries()) {
        if (Array.isArray(stage)) {
            dockerText += `# Stage ${index}\n`;
            dockerText += stage.join('\n');

            const endString = index < data.length - 1
                ? '\n\n\n'
                : '\n';
            dockerText += endString;
            continue;
        }

        const stageString = composeStage(
            index,
            stage,
        );

        // Defense-in-depth: `composeStage` always returns a string or throws,
        // so this guards against ever appending the literal "undefined".
        if (typeof stageString !== 'string') {
            throw new Error(`Stage ${index} did not produce any output.`);
        }

        dockerText += stageString;
    }

    return dockerText;
}


const readDeonFile = async (
    file,
    Deon,
) => {
    try {
        const deon = new Deon.default();
        const parsedData = await deon.parseFile(file);

        return parsedData;
    } catch (error) {
        const reason = error && error.message ? error.message : error;
        throw new Error(`Could not read '.deon' file '${file}': ${reason}`);
    }
}


const writeDockerfile = async (
    data,
    target,
) => {
    const dockerText = generateDockerText(data);

    const targetPath = path.isAbsolute(target)
        ? target
        : path.join(process.cwd(), target);

    await fs.writeFile(
        targetPath,
        dockerText,
    );

    return dockerText;
}


const main = async () => {
    try {
        const {
            execSync,
         } = require('child_process');

        const root = execSync('npm root -g')
            .toString()
            .trim();


        const Deon = require(`${root}/@plurid/deon`);

        const source = process.argv[2] || 'dockerfile.deon';
        const target = process.argv[3] || 'Dockerfile';

        const parsedData = await readDeonFile(
            source,
            Deon,
        );

        await writeDockerfile(
            parsedData,
            target,
        );

        return;
    } catch (error) {
        const reason = error && error.message ? error.message : error;
        console.error(`docker-deon failed: ${reason}`);
        console.error(
            `Ensure that '@plurid/deon' is installed and functional · https://manual.plurid.com/deon/getting-started`,
        );
        process.exit(1);
    }
}


if (require.main === module) {
    main();
}


module.exports = {
    INJECTION_PATTERN,
    assertNoInjection,
    validateParsedData,
    composeStage,
    generateDockerText,
    readDeonFile,
    writeDockerfile,
};
